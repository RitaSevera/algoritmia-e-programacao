package FichaPratica10.Exercicio06;

public class Atleta {
    private String nome;
    private String modalidade;
    private String pais;
    private double altura;
    private double peso;

    public Atleta(String nome, String modalidade, String pais, double altura, double peso) {
        this.nome = nome;
        this.modalidade = modalidade;
        this.pais = pais;
        this.altura = altura;
        this.peso = peso;
    }
}
